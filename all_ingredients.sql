-- Ингредиенты к рецепту
CREATE TABLE IF NOT EXISTS public.ingredients (
	ingredient_id SERIAL PRIMARY KEY,
	ingredient_name VARCHAR(50),
	measure VARCHAR(50), -- в чем измеряется
	category VARCHAR(100)
);

-- Сам рецепт
CREATE TABLE IF NOT EXISTS public.recipies (
    recipe_id SERIAL PRIMARY KEY,
    photo_link TEXT,
    recipe_name VARCHAR(100),
    cook_time VARCHAR(50),
    instruction TEXT,
	tag TEXT
);

-- Таблица для отношений между рецептами и ингредиентами
CREATE TABLE IF NOT EXISTS public.recipe_ingredients (
    recipe_id INT REFERENCES public.recipies(recipe_id),
    ingredient_id INT REFERENCES public.ingredients(ingredient_id),
    ingredient_amount VARCHAR(50), -- количество + в чем измеряется
    PRIMARY KEY (recipe_id, ingredient_id)
);



